// variables in JS
// to define variables in JS we use 'var' or 'let'
//'const' is use to define constant variable 
// constant varibales are unchangeable 

//example
var num = 12
let user_status = true 

// to print data in console 
console.log(num)
console.log(user_status)

// note 
// 1. var is use for global scope and let is use for block scope 
// 2. varibale defined within var can be re-declared and update 
// 3. varibale defined with let can be updated but cant be re-decalred